std::string CodeGen::generate_header(
    const std::string& source_file,
    const std::vector<DeviceInstance>& devices,
    const std::vector<Connection>& connections,
    const std::unordered_map<std::string, uint32_t>& port_to_signal,
    uint32_t signal_count
) {
    std::ostringstream oss;

    // Header guard
    std::string guard = "GENERATED_" + sanitize_name(source_file);
    std::replace(guard.begin(), guard.end(), '/', '_');
    std::replace(guard.begin(), guard.end(), '\\', '_');
    guard += "_H";

    oss << "// Auto-generated by codegen from " << source_file << "\n";
    oss << "// DO NOT EDIT - this will be overwritten on next build\n\n";
    oss << "#pragma once\n\n";
    oss << "#include <cstdint>\n";
    oss << "#include <string>\n";
    oss << "#include <array>\n";
    oss << "#include \"jit_solver/state.h\"\n";
    oss << "#include \"jit_solver/components/all.h\"\n\n";
    oss << "namespace an24 {\n\n";

    // Types are already defined in all.h - don't regenerate them

    // Signal constants
    oss << "// ==============================================================================\n";
    oss << "// SIGNAL INDICES\n";
    oss << "// ==============================================================================\n\n";

    for (const auto& [port, sig] : port_to_signal) {
        std::string const_name = "SIG_" + sanitize_name(to_upper(port));
        oss << "constexpr uint32_t " << const_name << " = " << sig << ";\n";
    }
    oss << "\n";

    // Fixed signals - RefNode v_out ports
    oss << "/// Fixed signal indices (RefNode bus voltages)\n";
    oss << "constexpr uint32_t FIXED_SIGNALS[] = {";
    bool first = true;
    for (const auto& dev : devices) {
        if (dev.internal == "RefNode") {
            // Try v_out first, fall back to v
            std::string port_key = dev.name + ".v_out";
            if (port_to_signal.count(port_key)) {
                if (!first) oss << ", ";
                oss << port_to_signal.at(port_key);
                first = false;
            } else {
                port_key = dev.name + ".v";
                if (port_to_signal.count(port_key)) {
                    if (!first) oss << ", ";
                    oss << port_to_signal.at(port_key);
                    first = false;
                }
            }
        }
    }
    oss << "};\n\n";

    // Signal count
    oss << "/// Total number of unique signals (for memory allocation)\n";
    oss << "constexpr uint32_t SIGNAL_COUNT = " << signal_count << ";\n\n";

    // Systems class
    oss << "// ==============================================================================\n";
    oss << "// SYSTEMS CLASS\n";
    oss << "// ==============================================================================\n\n";

    oss << "class Systems {\n";
    oss << "public:\n";

    // Device fields
    for (const auto& dev : devices) {
        oss << "    " << dev.internal << " " << dev.name << ";\n";
    }
    oss << "\n";

    // Constructor
    oss << "    Systems();\n\n";

    // Methods
    oss << "    void pre_load();\n";
    oss << "    void solve_step(void* state, uint32_t step);\n";
    oss << "    void post_step(void* state, float dt);\n";
    oss << "    uint32_t component_count() const { return " << devices.size() << "; }\n";

    oss << "};\n\n";

    oss << "} // namespace an24\n";

    return oss.str();
}

std::string CodeGen::generate_source(
    const std::string& header_name,
    const std::vector<DeviceInstance>& devices,
    const std::vector<Connection>& connections,
    const std::unordered_map<std::string, uint32_t>& port_to_signal,
    uint32_t signal_count
) {
    std::ostringstream oss;

    oss << "#include \"" << header_name << "\"\n\n";
    oss << "namespace an24 {\n\n";

    // Constructor implementation - use designated initializers
    oss << "Systems::Systems()\n";
    oss << "{\n";

    for (size_t i = 0; i < devices.size(); ++i) {
        const auto& dev = devices[i];

        // Port indices - map port names to C++ field names
        std::vector<std::string> port_names;
        for (const auto& p : dev.ports) {
            port_names.push_back(p.first);
        }
        std::sort(port_names.begin(), port_names.end());

        for (const auto& port_name : port_names) {
            std::string port_key = dev.name + "." + port_name;
            uint32_t sig = port_to_signal.count(port_key) ? port_to_signal.at(port_key) : signal_count;

            // Map port name to C++ field name
            std::string field_name = port_name;
            if (dev.internal == "Bus") {
                // Bus uses bus_idx
                field_name = "bus_idx";
            } else if (dev.internal == "RefNode") {
                // RefNode uses node_idx
                field_name = "node_idx";
            } else if (dev.internal == "IndicatorLight" && port_name == "brightness") {
                // IndicatorLight uses brightness_idx
                field_name = "brightness_idx";
            } else {
                // Default: port_name + "_idx"
                field_name = port_name + "_idx";
            }

            oss << "    " << dev.name << "." << field_name << " = " << sig << ";\n";
        }

        // Parameters - only those that differ from defaults
        // Skip inv_capacity - it's computed in pre_load
        for (const auto& param_name : dev.params) {
            const auto& value = param_name.second;
            if (param_name.first == "internal_r" || param_name.first == "inv_internal_r" || param_name.first == "inv_capacity") continue;
            std::string type = infer_type(value);
            oss << "    " << dev.name << "." << param_name.first << " = " << format_value(value, type) << ";\n";
        }
    }
    oss << "}\n\n";

    // pre_load
    oss << "void Systems::pre_load() {\n";
    for (const auto& dev : devices) {
        if (dev.internal == "Battery") {
            // Precompute inv_internal_r = 1.0f / internal_r
            if (dev.params.count("internal_r")) {
                oss << "    " << dev.name << ".inv_internal_r = 1.0f / " << dev.name << ".internal_r;\n";
            }
        }
    }
    oss << "}\n\n";

    // solve_step - call solve_electrical on each component
    oss << "void Systems::solve_step(void* state, uint32_t step) {\n";
    oss << "    auto* st = static_cast<SimulationState*>(state);\n";
    oss << "    (void)step;\n\n";
    for (const auto& dev : devices) {
        oss << "    " << dev.name << ".solve_electrical(*st);\n";
    }
    oss << "}\n\n";

    // post_step - call post_step on each component
    oss << "void Systems::post_step(void* state, float dt) {\n";
    oss << "    auto* st = static_cast<SimulationState*>(state);\n";
    for (const auto& dev : devices) {
        oss << "    " << dev.name << ".post_step(*st, dt);\n";
    }
    oss << "}\n\n";

    oss << "} // namespace an24\n";

    return oss.str();
}

void CodeGen::write_files(
    const std::string& out_dir,
    const std::string& source_file,
    const std::vector<DeviceInstance>& devices,
    const std::vector<Connection>& connections,
    const std::unordered_map<std::string, uint32_t>& port_to_signal,
    uint32_t signal_count
) {
    // Generate header name from source file
    std::string base_name = source_file;
    size_t pos = base_name.find_last_of("/\\");
    if (pos != std::string::npos) {
        base_name = base_name.substr(pos + 1);
    }
    pos = base_name.find_last_of('.');
    if (pos != std::string::npos) {
        base_name = base_name.substr(0, pos);
    }
    std::string header_name = "generated_" + base_name + ".h";

    std::string header_path = out_dir + "/" + header_name;
    std::string source_name = "generated_" + base_name + ".cpp";
    std::string source_path = out_dir + "/" + source_name;

    // Debug output
    std::cerr << "Writing header to: " << header_path << "\n";
    std::cerr << "Writing source to: " << source_path << "\n";

    // Generate header
    std::string header = generate_header(source_file, devices, connections, port_to_signal, signal_count);
    std::ofstream hfile(header_path);
    if (!hfile.is_open()) {
        std::cerr << "Failed to open: " << header_path << "\n";
        return;
    }
    hfile << header;
    hfile.close();

    // Generate source
    std::string source = generate_source(header_name, devices, connections, port_to_signal, signal_count);
    std::ofstream sfile(source_path);
    if (!sfile.is_open()) {
        std::cerr << "Failed to open: " << source_path << "\n";
        return;
    }
    sfile << source;
    sfile.close();
}

