std::string to_upper(const std::string& s) {
    std::string result = s;
    for (char& c : result) c = std::toupper(c);
    return result;
}

std::string sanitize_name(const std::string& s) {
    std::string result;
    for (char c : s) {
        if (c == '.' || c == '-' || c == ':') result += '_';
        else result += c;
    }
    return result;
}

// Infer C++ type from parameter value
std::string infer_type(const std::string& value) {
    if (value.empty()) return "float";

    // Check if it's an integer
    try {
        size_t pos;
        long long ival = std::stoll(value, &pos);
        if (pos == value.size()) {
            if (ival > INT32_MAX || ival < INT32_MIN) return "int64_t";
            return "int32_t";
        }
    } catch (...) {}

    // Check if it's a float
    try {
        size_t pos;
        float fval = std::stof(value, &pos);
        if (pos == value.size()) {
            return "float";
        }
    } catch (...) {}

    // Check if it's a bool
    if (value == "true" || value == "false") return "bool";

    // Default to string
    return "std::string";
}

// Format value for C++ code
std::string format_value(const std::string& value, const std::string& type) {
    if (type == "float") {
        try {
            float f = std::stof(value);
            std::ostringstream oss;
            oss << f;
            return oss.str();  // C++ infers float from context, no 'f' suffix needed
        } catch (...) {
            // Ensure decimal point for float literals: 28 -> 28.0
            std::string v = value;
            if (v.find('.') == std::string::npos) {
                v += ".0";
            }
            return v;
        }
    } else if (type == "bool") {
        return value;
    } else if (type == "int32_t" || type == "int64_t") {
        return value;
    } else if (type == "std::string") {
        return "std::string(\"" + value + "\")";
    } else {
        return "\"" + value + "\"";
    }
}

